const short int CLICKBATTLE[1104];
const short int PRESSRTOREADYr[360];
const short int PRESSBTOREADYb[360];
const short int READYr[140];
const short int READYb[140];
const short int RWINSr[140];
const short int BWINSb[140];
const short int pressSpaceToContinuerb[530];
const short int chillModePress1[506];
const short int intenseModePress2[616];
const short int pressBackspaceToStart[560];
const short int pressShiftToStart[465];
bool Rwins = false;
bool endGame = false;

int main(void) {
  volatile int* pixel_ctrl_ptr = (int*)0xFF203020;
  *(pixel_ctrl_ptr + 1) = (int)&Buffer1;
  wait_for_sync();  // waits for vsync
  pixel_buffer_start = *(pixel_ctrl_ptr + 1);
  clear_screen();
  startScreen();
  *(pixel_ctrl_ptr + 1) = (int)&Buffer2;
  pixel_buffer_start = *(pixel_ctrl_ptr + 1);  // we draw on the back buffer
  clear_screen();  // pixel_buffer_start points to the pixel buffer
  startScreen();

  wait_for_sync();
  pixel_buffer_start = *(pixel_ctrl_ptr + 1);  // switch back to back buffer
  volatile int* PS2_ptr = (int*)PS2_BASE;
  volatile int* LED_ptr = (int*)LED_BASE;

  int Rkey = R_INPUT_HEX;
  int Bkey = B_INPUT_HEX;
  int key1 = ONE_INPUT_HEX;
  int key2 = TWO_INPUT_HEX;
  int key_break = BREAK_INPUT_HEX;
  int space = SPACE_INPUT_HEX;

  int xWins = 146, yWins = 120, dx, dy;

  int PS2_data, RVALID;
  int keydata = 0;
  *(PS2_ptr) = 0xFF;  // resets the input

  bool key_released_break = false;
  int key_released;
  int old_gameLoc = gameLoc;
  short int colour = blue;

  bool modeChosen = false;
  int CLICKINCREMENT = CLICK_INCREMENT;

  while (1) {
    modeChosen = false;
    xWins = 146;
    yWins = 120;
    bool Rwon = false;
    startScreen();
    drawCLICKBATTLE();
    drawChillMode();
    drawIntenseMode();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);
    startScreen();
    drawCLICKBATTLE();
    drawChillMode();
    drawIntenseMode();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);
    while (!modeChosen) {
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        keydata = PS2_data & 0xFF;  // extract which key clicked
        if (keydata == key1) {
          CLICKINCREMENT = CLICK_INCREMENT / 2;
          modeChosen = true;
        } else if (keydata == key2) {
          CLICKINCREMENT = CLICK_INCREMENT;
          modeChosen = true;
        }
        wait_for_sync();
        pixel_buffer_start = *(pixel_ctrl_ptr + 1);  // switch back to back
        // buffer
      }
    }

    int waitCounter = 2;
    while (waitCounter > 0) {
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        waitCounter = waitCounter - 1;
      }
    }
    deleteChillMode();
    deleteIntenseMode();
    startScreen();
    drawCLICKBATTLE();
    // drawRToStart();
    // drawBToStart();
    drawShiftToStart();
    drawBackspaceToStart();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);
    deleteChillMode();
    deleteIntenseMode();
    startScreen();
    drawCLICKBATTLE();
    // drawRToStart();
    // drawBToStart();
    drawShiftToStart();
    drawBackspaceToStart();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);

    // flags for whether to display loading messages for r or b or to display
    // ready message
    bool readyB = false;
    bool readyR = false;
    gameLoc = (SCREENLEFT + SCREENRIGHT) / 2;  // middle of the screen
    // loop for players readying
    while (!readyB || !readyR) {
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        keydata = PS2_data & 0xFF;  // extract which key clicked
        if (keydata == Rkey && !readyR) {
          //   deleteRToStart();
          deleteShiftToStart();
          drawReadyR();
          wait_for_sync();
          pixel_buffer_start = *(pixel_ctrl_ptr + 1);
          //   deleteRToStart();
          deleteShiftToStart();
          drawReadyR();
          readyR = true;
        } else if (keydata == Bkey && !readyB) {
          //   deleteBToStart();
          deleteBackspaceToStart();
          drawReadyB();
          wait_for_sync();
          pixel_buffer_start = *(pixel_ctrl_ptr + 1);
          //   deleteBToStart();
          deleteBackspaceToStart();
          drawReadyB();
          readyB = true;
        }
        wait_for_sync();
        pixel_buffer_start = *(pixel_ctrl_ptr + 1);  // switch back to back
        // buffer
      }
    }
    // bool bufferNotEmpty = true;
    waitCounter = 2;
    while (waitCounter > 0) {
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        waitCounter = waitCounter - 1;
      }
    }
    // potentially show 3 2 1 go message with internal clock
    // delete start screen messages
    deleteCLICKBATTLE();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);
    deleteCLICKBATTLE();
    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);

    // actual gameplay loop - in game if gameLoc is in bounds of screen,
    // otherwise game over
    gameLoc = (SCREENLEFT + SCREENRIGHT) / 2;
    old_gameLoc = gameLoc;

    while (gameLoc > SCREENLEFT && gameLoc < SCREENRIGHT) {
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        keydata = PS2_data & 0xFF;
        if (key_released_break == true) {
          key_released = keydata;
          key_released_break = false;
        }
        if (keydata == key_break) {
          key_released_break = true;
        }
      }

      if (key_released != 0) {
        old_gameLoc = gameLoc;
        if (key_released == Bkey) {
          gameLoc = gameLoc - CLICKINCREMENT;
          colour = blue;
        } else if (key_released == Rkey) {
          gameLoc = gameLoc + CLICKINCREMENT;
          colour = red;
        }
        key_released = 0;
      }

      draw_line(old_gameLoc, 0, old_gameLoc, 240, colour);

      if (gameLoc > old_gameLoc) {
        draw_rect(gameLoc - CLICKINCREMENT, 0, CLICKINCREMENT, 240, colour);
      } else if (gameLoc < old_gameLoc) {
        draw_rect(gameLoc, 0, CLICKINCREMENT, 240, colour);
      }

      wait_for_sync();
      pixel_buffer_start = *(pixel_ctrl_ptr + 1);  // switch back to back buffer
    }

    // this repetition is necessary so that the second buffer is updated after
    // the game is won or else there will be flickering
    draw_line(old_gameLoc, 0, old_gameLoc, 240, colour);
    if (gameLoc > old_gameLoc) {
      draw_rect(gameLoc - CLICKINCREMENT, 0, CLICKINCREMENT, 240, colour);
    } else if (gameLoc < old_gameLoc) {
      draw_rect(gameLoc, 0, CLICKINCREMENT, 240, colour);
    }
    // post game message of who won
    if (gameLoc <= SCREENLEFT) {
      Rwon = false;
    } else if (gameLoc >= SCREENRIGHT) {
      Rwon = true;
    } else
      printf("Error\n");
    // display R WINS!!! / B WINS!!! message based on Rwon boolean value and
    // internal timer

    bool nextGame = false;
    endGame = true;
    Rwins = Rwon;

    // display who WINS!!!
    dx = ((rand() % 2) * 2) - 1;
    dy = ((rand() % 2) * 2) - 1;
    while (!nextGame) {
      deleteWins(xWins, yWins, colour);
      deleteWins(xWins - dx, yWins - dy, colour);

      xWins += dx;
      yWins += dy;
      if ((xWins >= SCREENRIGHT - 28) || (xWins <= 0)) dx *= -1;
      if ((yWins >= 240 - 5) || (yWins <= 0)) dy *= -1;
      if (xWins >= 106 - 28 && xWins <= SCREENRIGHT - 106 &&
          yWins >= SCREENBOTTOM - 10)
        dy *= -1;
      if (yWins >= SCREENBOTTOM - 10 &&
          (xWins == 106 - 28 || xWins == SCREENRIGHT - 106))
        dx *= -1;

      drawSpaceToContinue();
      if (Rwon) {
        drawRWins(xWins, yWins, dx, dy);
      } else {
        drawBWins(xWins, yWins, dx, dy);
      }

      wait_for_sync();
      pixel_buffer_start = *(pixel_ctrl_ptr + 1);
      PS2_data = *(PS2_ptr);       // read the Data register in the PS/2 port
      RVALID = PS2_data & 0x8000;  // extract the RVALID field
      if (RVALID) {
        keydata = PS2_data & 0xFF;
        if (keydata == space) {
          dx = 0;
          dy = 0;
          endGame = false;
          nextGame = true;
        }
      }
    }

    wait_for_sync();
    pixel_buffer_start = *(pixel_ctrl_ptr + 1);
    // poll for space bar to play again
  }

  return 0;
}

void startScreen() {
  for (int i = 0; i < 160; i++) {
    for (int j = 0; j < 240; j++) {
      plot_pixel(i, j, red);
    }
  }
  for (int i = 160; i < 320; i++) {
    for (int j = 0; j < 240; j++) {
      plot_pixel(i, j, blue);
    }
  }
}

void draw_box(int x, int y, int width, short int colour) {
  for (int i = 0; i < width; i++) {
    draw_line(x, y + i, x + width, y + i, colour);
  }
}

void draw_rect(int x, int y, int width, int height, short int colour) {
  for (int i = 0; i < height; i++) {
    draw_line(x, y + i, x + width, y + i, colour);
  }
}
void wait_for_sync() {  // waits for V-sync and rendering to finish
  volatile int* pixel_ctrl_ptr = (int*)0xFF203020;
  int status;
  *pixel_ctrl_ptr = 1;
  status = *(pixel_ctrl_ptr + 3);
  while ((status & 0x01) != 0) {
    status = *(pixel_ctrl_ptr + 3);
  }
}

void draw_line(int x0, int y0, int x1, int y1, short int line_color) {
  int x, y, is_steep;
  if (x1 == x0) {
    is_steep = 2;
  } else {
    is_steep = abs(y1 - y0) / abs(x1 - x0);
  }
  if (is_steep > 1) {
    swap(&x0, &y0);
    swap(&x1, &y1);
  }
  if (x0 > x1) {
    swap(&x0, &x1);
    swap(&y0, &y1);
  }

  for (x = x0; x <= x1; x++) {
    y = y0 + (10 * (y1 - y0) * (x - x0) / (x1 - x0) + 5) / 10;
    if (is_steep > 1) {
      plot_pixel(y, x, line_color);
    } else {
      plot_pixel(x, y, line_color);
    }
  }
}

void swap(int* x, int* y) {
  int temp;
  temp = *x;
  *x = *y;
  *y = temp;
}

int abs(int x) {
  if (x < 0) {
    return x = x * -1;
  } else {
    return x;
  }
}

void plot_pixel(int x, int y, short int line_color) {
  volatile short int* one_pixel_address =
      pixel_buffer_start + (y << 10) + (x << 1);
  *one_pixel_address = line_color;
}

void clear_screen() {
  int x, y;
  for (x = 0; x < 320; x++) {
    for (y = 0; y < 240; y++) {
      plot_pixel(x, y, 0);
    }
  }
}

